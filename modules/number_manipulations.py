from math import * #importing an outside package to increase the number of functions in your system.

#basic printing of numbers
print(32)
print(-32)
print(23.23)

#storing value in variable
number_example = 12232002.32
print(number_example)

#basic operations for number manipulations
print(number_example + 32) #addition
print(number_example - 32) #subraction
print(number_example * 32) #multiplication
print(number_example / 32) #division
print(number_example % 32) #modulo - printing the remainder of the quotient

#some manipulations
print(pow(number_example, 2)) #raising the power to 2 - value^2 (something like that)
print(ceil(number_example)) #round up your number if it has decimal value
print(sqrt(number_example)) #provide the square root of the value